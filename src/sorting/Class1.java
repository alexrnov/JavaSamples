package sorting;

public class Class1 {
  private String name;
  private Integer age;

  public Class1(String name, Integer age) {
    this.name = name;
    this.age = age;
  }

  public void print() {
    System.out.println(name + " " + age);
  }

  public String getName() {
    return name;
  }

  public Integer getAge() {
    return age;
  }
}
